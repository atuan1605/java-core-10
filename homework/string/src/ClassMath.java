public class ClassMath {
    public static void main(String[] args){
        System.out.println("pi = " + Math.PI);
        System.out.println("Số lớn nhất trong hai số: " + Math.max(6,9));
        System.out.println("Số nhỏ nhất trong hai số: " + Math.min(9,4));

        System.out.println("số ngẫu nhiên: " + Math.random());
        System.out.println("khai căn: " + Math.sqrt(9));
        System.out.println("Lũy thừa: " + Math.pow(6, 3));
    }

}
